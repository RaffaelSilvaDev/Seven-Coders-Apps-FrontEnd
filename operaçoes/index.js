var resultado;
var x = 2;
var y = 5;

console.log("Operações Básicas Matemáticas")
// soma
resultado = x + y;
console.log("A soma é:", resultado);

// subtração
resultado = x - y;
console.log("A subtração é:", resultado);

// multiplicação 
resultado = x * y;
console.log("A multiplicação é:", resultado);

// divisão
resultado = x / y;
console.log("A divisão é:", resultado);


// cálculo do módulo (resto da divisão)
resultado = x % y;
console.log("Módulo:", resultado)
  
// Cálculo Avançado
var peso = 85;
var altura = 1.67;
var imc = (peso / (altura * altura));
console.log("IMC:", imc);

// soma de strings
var primeiroNome = "Rafael";
var segundoNome = " da Silva";
console.log("Nome Completo:", primeiroNome + segundoNome);

